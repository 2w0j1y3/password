u32 SM4_rotl(u32 x, int n) {
	return (x << n) | (x >> (32 - n));
}

u32 SM4_sub(u32 x) {
	return (SM4_SBOX[(x>>24)&0xFF] << 24) |
	(SM4_SBOX[(x>>16)&0xFF] << 16) |
	(SM4_SBOX[(x>>8)&0xFF] << 8) |
	SM4_SBOX[x&0xFF];
}

void SM4_set_key(const u8 *key, u32 *rk) {
	u32 FK[4] = {0xA3B1BAC6, 0x56AA3350, 0x677D9197, 0xB27022DC};
	u32 CK[32] = {
		0x00070E15,0x1C232A31,0x383F464D,0x545B6269,0x70777E85,0x8C939AA1,0xA8AFB6BD,0xC4CBD2D9,
		0xE0E7EEF5,0xFC030A11,0x181F262D,0x343B4249,0x50575E65,0x6C737A81,0x888F969D,0xA4ABB2B9,
		0xC0C7CED5,0xDCE3EAF1,0xF8FF060D,0x141B2229,0x30373E45,0x4C535A61,0x686F767D,0x848B9299,
		0xA0A7AEB5,0xBCC3CAD1,0xD8DFE6ED,0xF4FB0209,0x10171E25,0x2C333A41,0x484F565D,0x646B7279
	};
	u32 k[4];
	for(int i=0;i<4;i++){
		k[i] = ((u32)key[i*4+0] << 24) |
		((u32)key[i*4+1] << 16) |
		((u32)key[i*4+2] << 8) |
		((u32)key[i*4+3]);
	}
	for(int i=0;i<4;i++) k[i] ^= FK[i];
	for(int i=0;i<32;i++){
		u32 t = k[1] ^ k[2] ^ k[3] ^ CK[i];
		t = SM4_sub(t);
		rk[i] = k[0] ^ SM4_rotl(t, 2) ^ SM4_rotl(t, 10) ^ SM4_rotl(t, 18) ^ SM4_rotl(t, 24);
		k[0] = k[1]; k[1] = k[2]; k[2] = k[3]; k[3] = rk[i];
	}
}

void SM4_encrypt(const u32 *rk, const u8 *in, u8 *out) {
	u32 x[4];
	for(int i=0;i<4;i++){
		x[i] = ((u32)in[i*4+0] << 24) |
		((u32)in[i*4+1] << 16) |
		((u32)in[i*4+2] << 8) |
		((u32)in[i*4+3]);
	}
	for(int i=0;i<32;i++){
		u32 t = x[1] ^ x[2] ^ x[3] ^ rk[i];
		t = SM4_sub(t);
		u32 tmp = x[0] ^ SM4_rotl(t, 2) ^ SM4_rotl(t, 10) ^ SM4_rotl(t, 18) ^ SM4_rotl(t, 24);
		x[0] = x[1]; x[1] = x[2]; x[2] = x[3]; x[3] = tmp;
	}
	for(int i=0;i<4;i++){
		out[i*4+0] = (x[3-i] >> 24) & 0xFF;
		out[i*4+1] = (x[3-i] >> 16) & 0xFF;
		out[i*4+2] = (x[3-i] >> 8) & 0xFF;
		out[i*4+3] = x[3-i] & 0xFF;
	}
}

inline int rand_int(int l, int r) {
	static mt19937 rng(random_device{}());
	return uniform_int_distribution<>(l, r)(rng);
}

inline void gen_sp(string& out) {
	int len = rand_int(SP_MIN_LEN, SP_MAX_LEN);
	for (int i = 0; i < len; ++i)
		out += sp_chars[rand_int(0, (int)sp_chars.size()-1)];
}

inline void to_base_str(int x, int base, string& out) {
	char buf[32]; int idx=0;
	if(x==0) buf[idx++]='0';
	else while(x>0){
		int rem=x%base;
		buf[idx++] = (rem<10) ? ('0'+rem) : ('A'+rem-10);
		x/=base;
	}
	out += char((base<10) ? ('0'+base) : ('A'+base-10));
	while(idx>0) out += buf[--idx];
}

// SM4 流密码
vector<int> sm4_keystream(int seed, int len) {
	u8 key[16] = {0};
	for(int i=0;i<4;i++) key[i] = (seed >> (i*8)) & 0xFF;
	u32 rk[32];
	SM4_set_key(key, rk);
	
	vector<int> ks;
	ks.reserve(len);
	u8 buf[16] = {0};
	while((int)ks.size() < len) {
		for(int i=0;i<16;i++) buf[i]++;
		u8 out[16];
		SM4_encrypt(rk, buf, out);
		for(int b : out) {
			if((int)ks.size() >= len) break;
			ks.push_back(b);
		}
	}
	return ks;
}

string encrypt(const string& plain_text) {
	string cipher;
	cipher.reserve(plain_text.size()*8);
	int fir = rand_int(0, INT_MAX);
	auto ks = sm4_keystream(fir, plain_text.size());
	
	gen_sp(cipher);
	to_base_str(fir, rand_int(BASE_MIN, BASE_MAX), cipher);
	
	for(int i=0;i<(int)plain_text.size();i++){
		gen_sp(cipher);
		to_base_str( (u8)plain_text[i] ^ ks[i], rand_int(BASE_MIN, BASE_MAX), cipher);
	}
	return cipher;
}

string decrypt(const string& cipher_text) {
	int pos=0, len=cipher_text.size();
	string plain;
	plain.reserve(len/4);
	
	auto to_val = [](char c) {
		return isdigit(c) ? (c-'0') : (c-'A'+10);
	};
	auto is_sp = [](char c) {
		return !((c>='0'&&c<='9')||(c>='A'&&c<='Z'));
	};
	auto getb = [&]() -> int {
		while(pos<len && is_sp(cipher_text[pos])) pos++;
		if(pos>=len) return -1;
		int base=to_val(cipher_text[pos++]);
		int x=0;
		while(pos<len && !is_sp(cipher_text[pos]))
			x = x*base + to_val(cipher_text[pos++]);
		return x;
	};
	
	int fir=getb();
	if(fir == -1) return "";
	
	vector<int> ks;
	int cnt=0;
	while(true){
		int val=getb();
		if(val==-1) break;
		if(ks.empty()) ks = sm4_keystream(fir, 100000);
		plain += (char)( val ^ ks[cnt++] );
	}
	return plain;
}

